<script>
export default {
  name: "navbar",
  data() {
    return {
      theme: "light"
    };
  },
  mounted: function() {
    this.setInitialTheme();
  },
  methods: {
    buttonOnClickScroll(loc) {
      var section = document.querySelector(`#${loc}`);
      section.scrollIntoView({ behavior: "smooth" });
    },
    toggleTheme() {
      if (this.theme === "dark") {
        document.documentElement.setAttribute("theme", "light");
        localStorage.setItem("theme", "light");
        this.theme = "light";
      } else {
        document.documentElement.setAttribute("theme", "dark");
        localStorage.setItem("theme", "dark");
        this.theme = "dark";
      }
    },
    setInitialTheme() {
      if (localStorage.getItem("theme")) {
        const cachedTheme = localStorage.getItem("theme");
        document.documentElement.setAttribute("theme", cachedTheme);
        this.theme = cachedTheme;
      } else {
        document.documentElement.setAttribute("theme", this.theme);
      }
    },
    openDrawer() {
      document.querySelector("#drawer").style.opacity = "1";
      document.querySelector("#drawer").style.right = "0";
    },
    closeDrawer() {
      document.querySelector("#drawer").style.opacity = "0";
      document.querySelector("#drawer").style.right = "-80vw";
    }
  }
};
</script>

